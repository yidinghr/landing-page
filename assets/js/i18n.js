  let currentLocale = loadInitialLocale();

  function loadInitialLocale() {
    try {
      const stored = window.localStorage.getItem(STORAGE_KEY);
      return normalizeLocale(stored);
    } catch (error) {
      return DEFAULT_LOCALE;
    }
  }

  function normalizeLocale(locale) {
    return SUPPORTED_LOCALES.indexOf(locale) >= 0 ? locale : DEFAULT_LOCALE;
  }

  function setDocumentLanguage() {
    document.documentElement.lang = currentLocale;
  }

  function interpolate(template, params) {
    if (!params) {
      return template;
    }

    return String(template).replace(/\{([^}]+)\}/g, function (_, token) {
      return params[token] !== undefined ? String(params[token]) : "";
    });
  }

  function t(key, params) {
    const localeTable = translations[currentLocale] || {};
    const fallbackTable = translations[DEFAULT_LOCALE] || {};
    const template = localeTable[key] !== undefined ? localeTable[key] : (fallbackTable[key] !== undefined ? fallbackTable[key] : key);
    return interpolate(template, params);
  }

  function translateLiteral(value) {
    if (currentLocale === DEFAULT_LOCALE) {
      return value;
    }

    const localeTable = literalTranslations[currentLocale] || {};
    return localeTable[value] !== undefined ? localeTable[value] : value;
  }

  function getLocale() {
    return currentLocale;
  }

  function setLocale(locale) {
    const nextLocale = normalizeLocale(locale);

    if (nextLocale === currentLocale) {
      return currentLocale;
    }

    currentLocale = nextLocale;

    try {
      window.localStorage.setItem(STORAGE_KEY, currentLocale);
    } catch (error) {
      // ignore storage failure
    }

    setDocumentLanguage();
    subscribers.forEach(function (listener) {
      listener(currentLocale);
    });

    return currentLocale;
  }

  function subscribe(listener) {
    if (typeof listener !== "function") {
      return function () {};
    }

    subscribers.add(listener);
    return function () {
      subscribers.delete(listener);
    };
  }

  function getLocaleOptions() {
    return LOCALE_OPTIONS.slice();
  }

  function isDefaultValue(value, defaults) {
    return defaults.indexOf(String(value || "")) >= 0;
  }

  setDocumentLanguage();

  window.YiDingI18n = {
    STORAGE_KEY: STORAGE_KEY,
    DEFAULT_LOCALE: DEFAULT_LOCALE,
    SUPPORTED_LOCALES: SUPPORTED_LOCALES.slice(),
    getLocale: getLocale,
    setLocale: setLocale,
    t: t,
    translateLiteral: translateLiteral,
    subscribe: subscribe,
    getLocaleOptions: getLocaleOptions,
    isDefaultValue: isDefaultValue
  };
